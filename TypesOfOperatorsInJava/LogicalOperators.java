package TypesOfOperatorsInJava;

public class LogicalOperators {
  public static void main(String[] args) {
    // Logical operators
    boolean x = true;
    boolean y = false;

    System.out.println("x && y: " + (x && y));
    System.out.println("x || y: " + (x || y));
    System.out.println("!x: " + (!x));
  }

}
